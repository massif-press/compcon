<template>
  <div class="d-inline-block">
    <v-menu offset-x left>
      <template #activator="{ props }">
        <v-icon icon dark class="fade-select mt-n1" v-bind="props">mdi-cog</v-icon>
      </template>
      <v-list density="compact">
        <v-list-item :disabled="item.Tier === 3" @click="upgradeTier()">
          <v-list-item-icon class="ma-0 mr-2 mt-3">
            <v-icon icon="mdi-arrow-up-thick" />
          </v-list-item-icon>
          <v-list-item-content>
            <v-list-item-title>Upgrade Tier</v-list-item-title>
          </v-list-item-content>
        </v-list-item>
        <v-list-item :disabled="item.Tier === 1" @click="downgradeTier()">
          <v-list-item-icon class="ma-0 mr-2 mt-2">
            <v-icon icon="mdi-arrow-down-thick" />
          </v-list-item-icon>
          <v-list-item-content>
            <v-list-item-title>Downgrade Tier</v-list-item-title>
          </v-list-item-content>
        </v-list-item>
        <v-list-item @click="($refs as any).cName.show()">
          <v-list-item-icon class="ma-0 mr-2 mt-2">
            <v-icon icon="mdi-circle-edit-outline" />
          </v-list-item-icon>
          <v-list-item-content>
            <v-list-item-title>Set Custom Name</v-list-item-title>
          </v-list-item-content>
        </v-list-item>
        <v-list-item @click="($refs as any).cDesc.show()">
          <v-list-item-icon class="ma-0 mr-2 mt-2">
            <v-icon icon="mdi-circle-edit-outline" />
          </v-list-item-icon>
          <v-list-item-content>
            <v-list-item-title>Set Custom Description</v-list-item-title>
          </v-list-item-content>
        </v-list-item>
        <div>
          <v-divider />
          <v-list-item @click="$emit('remove-feature', $event)">
            <v-list-item-icon class="ma-0 mr-2 mt-2">
              <v-icon color="error">mdi-delete</v-icon>
            </v-list-item-icon>
            <v-list-item-content>
              <v-list-item-title>Remove {{ item.Feature.FeatureType }}</v-list-item-title>
            </v-list-item-content>
          </v-list-item>
        </div>
      </v-list>
    </v-menu>
    <cc-string-edit-dialog
      ref="cName"
      :placeholder="item.Name"
      label="Custom Item Name"
      @save="save('Name', $event)"
      @reset="save('Name', '')"
    />
    <cc-string-edit-dialog
      ref="cDesc"
      :placeholder="item.Description"
      label="Custom Item Description"
      @save="save('Description', $event)"
      @reset="save('Description', '')"
    />
  </div>
</template>

<script lang="ts">
export default {
  name: 'npc-item-menu',
  props: {
    item: { type: Object, required: true },
  },
  methods: {
    upgradeTier() {
      this.item.Tier++;
      this.$emit('recalc');
    },
    downgradeTier() {
      this.item.Tier--;
      this.$emit('recalc');
    },
    save(prop, newName) {
      this.$set(this.item, prop, newName);
    },
  },
};
</script>
