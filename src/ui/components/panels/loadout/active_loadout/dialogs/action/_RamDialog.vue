<template>
  <div>
    <v-row justify="center" align="center">
      <v-col cols="12" md="">
        <action-detail-expander :action="action" />
      </v-col>
      <v-col cols="auto">
        <v-btn
          large
          tile
          dark
          block
          :disabled="used || actionFree"
          :color="`${action.Color} ${actionCost ? 'lighten-1' : ''}`"
          @click="actionCost = !actionCost"
        >
          <v-icon left>{{ action.Icon }}</v-icon>
          {{ action.Name }}
        </v-btn>
        <v-btn
          small
          tile
          dark
          block
          :disabled="actionCost"
          :color="`action--free ${actionFree ? 'lighten-1' : ''}`"
          @click="actionFree = !actionFree"
        >
          <v-icon left small>cci-free-action</v-icon>
          Free Action
          <cc-tooltip
            inline
            :content="`Special rules or equipment may allow you to ${action.Name} as a Free Action. Using this button will commit the action without spending a ${action.Activation} Action this turn`"
          >
            <v-icon right small class="fadeSelect">mdi-information-outline</v-icon>
          </cc-tooltip>
        </v-btn>
      </v-col>
    </v-row>

    <v-slide-x-reverse-transition>
      <v-row v-if="actionFree || actionCost" justify="center" align="center">
        <v-col lg="auto" md="12" class="mt-n5">
          <v-row
            dense
            class="text-center mb-n3"
            :justify="$vuetify.breakpoint.mdAndUp ? 'start' : 'space-around'"
            align="start"
          >
            <v-col cols="auto" :class="$vuetify.breakpoint.mdAndUp ? 'mx-8' : ''">
              <div class="overline mb-n2">Attack Roll</div>
              <div class="heading text--text" style="font-size: 24pt">
                <v-icon x-large class="mr-n1">mdi-dice-d20-outline</v-icon>
                + {{ mech.AttackBonus }}
              </div>
            </v-col>
            <v-col cols="auto" :class="$vuetify.breakpoint.mdAndUp ? 'mx-8' : ''">
              <div class="overline mb-n3">vs. Target</div>
              <v-icon x-large v-html="'cci-evasion'" />
              <div class="overline font-weight-bold mt-n2" v-html="'Evasion'" />
            </v-col>
          </v-row>
        </v-col>
        <v-col cols="auto" class="ml-auto">
          <v-row
            dense
            :justify="$vuetify.breakpoint.mdAndUp ? 'end' : 'space-around'"
            :class="$vuetify.breakpoint.mdAndUp ? '' : 'panel'"
          >
            <v-col
              cols="auto"
              :class="$vuetify.breakpoint.mdAndUp ? 'ml-auto px-12 mr-n10 panel dual-sliced' : ''"
              style="height: 70px"
            >
              <div class="overline pl-1">Accuracy</div>
              <v-text-field
                v-model="accuracy"
                type="number"
                append-outer-icon="mdi-plus-circle-outline"
                append-icon="cci-accuracy"
                prepend-icon="mdi-minus-circle-outline"
                style="width: 115px"
                class="hide-input-spinners"
                color="accent"
                dense
                hide-details
                @click:append-outer="accuracy += 1"
                @click:prepend="accuracy -= 1"
                @change="accuracy = parseInt($event)"
              />
            </v-col>
            <v-col
              cols="auto"
              :class="$vuetify.breakpoint.mdAndUp ? 'ml-auto px-12 mr-n10 panel dual-sliced' : ''"
              style="height: 70px"
            >
              <div class="overline pl-1">Difficulty</div>
              <v-text-field
                v-model="difficulty"
                type="number"
                append-outer-icon="mdi-plus-circle-outline"
                append-icon="cci-difficulty"
                prepend-icon="mdi-minus-circle-outline"
                style="width: 115px"
                class="hide-input-spinners"
                color="accent"
                dense
                hide-details
                @click:append-outer="difficulty += 1"
                @click:prepend="difficulty -= 1"
                @change="difficulty = parseInt($event)"
              />
            </v-col>
            <v-col
              cols="auto"
              :class="$vuetify.breakpoint.mdAndUp ? 'ml-auto px-12 panel dual-sliced' : ''"
              style="height: 70px"
            >
              <div class="overline pl-1">Melee Attack Roll</div>
              <v-row no-gutters>
                <v-col class="mr-n2 ml-n2">
                  <cc-dice-menu
                    :preset="`1d20+${mech.AttackBonus}`"
                    :preset-accuracy="accuracy - difficulty"
                    title="ATTACK ROLL"
                    autoroll=true
                    @commit="registerAttackRoll($event.total)"
                  />
                </v-col>
                <v-col>
                  <v-text-field
                    v-model="attackRoll"
                    :key="`input_${attackRoll}`"
                    type="number"
                    :class="`hide-input-spinners ml-n3 ${crit ? 'font-weight-bold' : ''}`"
                    style="max-width: 60px; margin-top: -0.5px"
                    :color="crit ? 'secondary' : 'accent'"
                    dense
                    hide-details
                  />
                </v-col>
              </v-row>
              <div v-if="crit" class="caption secondary--text font-weight-bold ml-8 my-n1">
                CRITICAL
              </div>
            </v-col>
          </v-row>
        </v-col>
      </v-row>
    </v-slide-x-reverse-transition>

    <v-slide-x-reverse-transition>
      <v-row v-if="attackRoll" dense class="mt-n2">
        <v-col md="6" lg="3" xl="2" class="ml-auto">
          <v-btn
            tile
            block
            class="primary"
            :color="`primary ${succeeded ? 'lighten-1' : ''}`"
            :disabled="failed"
            @click="succeeded = select(succeeded)"
          >
            SUCCESS
          </v-btn>
        </v-col>
        <v-col md="6" lg="3" xl="2">
          <v-btn
            tile
            block
            :disabled="succeeded"
            :color="failed ? 'error' : ''"
            @click="failed = select(failed)"
          >
            FAILURE
          </v-btn>
        </v-col>
      </v-row>
    </v-slide-x-reverse-transition>
    <v-slide-x-reverse-transition>
      <v-row v-if="succeeded" no-gutters class="mt-2">
        <v-col cols="auto" class="ml-auto" />
        <v-col v-if="crit" cols="auto" class="text-center">
          <cc-tooltip
            :content="`On a critical hit, all damage dice are rolled twice
  (including bonus damage) and the highest result from
  each source of damage is used.`"
          >
            <v-icon x-large color="secondary">mdi-progress-alert</v-icon>
            <div class="secondary--text">CRITICAL HIT</div>
          </cc-tooltip>
        </v-col>
        <v-col cols="auto" class="ml-auto" align="end">
          <div class="body-text stark--text font-weight-bold">
            Your target is knocked PRONE and you may also choose to knock them back by one space,
            directly away from you
          </div>
        </v-col>
      </v-row>
    </v-slide-x-reverse-transition>
  </div>
</template>

<script lang="ts">
import Vue from 'vue'
import ActionDetailExpander from '../../components/_ActionDetailExpander.vue'

export default Vue.extend({
  name: 'ram-dialog',
  components: { ActionDetailExpander },
  props: {
    used: { type: Boolean },
    mech: {
      type: Object,
      required: true,
    },
    action: {
      type: Object,
      required: true,
    },
  },
  data: () => ({
    dialog: false,
    accuracy: 0,
    difficulty: 0,
    attackRoll: '',
    succeeded: false,
    failed: false,
    complete: false,
    actionCost: false,
    actionFree: false,
  }),
  computed: {
    crit() {
      return this.attackRoll && this.attackRoll >= 20
    },
  },
  watch: {
    used: {
      immediate: true,
      deep: true,
      handler: function (newval) {
        if (!newval) this.init()
      },
    },
  },
  methods: {
    select(action) {
      this.$emit('use', this.actionFree)
      return !action
    },
    registerAttackRoll(roll) {
      Vue.set(this, 'attackRoll', roll)
      Vue.nextTick().then(() => this.$forceUpdate())
    },
    init() {
      this.accuracy = 0
      this.difficulty = 0
      this.attackRoll = ''
      this.succeeded = false
      this.failed = false
      this.actionCost = false
      this.actionFree = false
    },
  },
})
</script>
